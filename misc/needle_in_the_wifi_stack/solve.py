import base64

def decode_and_print(encoded_snippets):
    for encoded_snippet in encoded_snippets:
        try:
            # Decode the base64-encoded snippet
            decoded_snippet = base64.b64decode(encoded_snippet).decode('utf-8')
            
            # Print the decoded snippet
            print(decoded_snippet)
        except Exception as e:
            print(f"Error decoding snippet: {e}")

